/**
 * Desenvolvimento Ágil (SCUM)
 * Existe três membros envolvidos e eles são: SCRUM OWNER, SCRUM MASTER, SCRUM TEAM.
 * 
 * SCRUM OWNER: É basicamente o cliente que você está oferecendo ou desenvolvendo o serviço/produto. Nele pode conter
 * outros clientes (sub-clientes) que utilizaram seu serviço/produto.
 * 
 * SCRUM MASTER: Ele é o Gerente de Processos/Projeto/Gerente/Programador/Você depende do tamanho da equipe ou empresa.
 * O papel deste membro é a intermediação entre o SCRUM TEAM e o SCRUM OWNER, passando as necessidades do cliente
 * para o mundo da equipe desenvolvedora de serviços/produtos.
 * 
 * SCRUM TEAM: Este membro é basicamente a equipe que coloca a mão na massa, podendo ser de 1 até 6 pessoas por projeto
 * sem perder a produtividade.
 * 
 * Product Backlog: São as funcionalidades que o SCRUM OWNER necessita, transcrita em um ambiente de trabalho já dividias em prioridades.
 * 
 * Sprint Backlog: São as tarefas do Product Log subdivididas em processos que serão desenvolvidas pela equipe SCRUM TEAM. Normalmente a
 * Sprint tem de 5 a 28 dias, sendo totalmente flexível dependendo do projeto e tamanho da equipe.
 * 
 * Reuniões: Normalmente as reuniões acontecem sempre entre uma sprint e outra, a duração varia de projeto para projeto.
 * Nas reuniões os integrantes são o SCRUM MASTER e o SCRUM TEAM, esses decidem como será a organização e a execução do
 * Product Log para o Sprint Log.
 * 
 * Tipos de reuniões e quais questões serão discutidas:
 * 1) Stand Up Meeting
 * - Todos de pé.
 * - Logo após o almoço.
 * - Diariamente.
 * - Responder 3 perguntas. "O que você fez ontem em relação ao projeto?". "O que planeja fazer hoje?". "O projeto tem algum problema?
 * (discutir com os companheiros de equipe e o SCRUM MASTER)". 
 * 
 * 2) Início de sprint(2 etapas)
 *  - I) Reunião entre o SCRUM MASTER e o SCRUM OWNER, vão priorizar o que deve ser feito na Sprint visualizando
 *    o Product Log.
 *  - II) Reunião entre o SCRUM MASTER e o SCRUM TEAM, vai definir prazos, organização da equipe e tarefa de cada membro.
 * 
 * 3) Revisão de Sprint
 * - Mostrar para o cliente.
 * - Rever tudo o que foi feito.
 * - O que não deu tempo para fazer.
 * - O que deve ser passado para o próximo Product Back Log.
 * 
 * 4) Retrospectiva
 * - Tudo o que foi feito.
 * - Melhorias no processo.
 */
